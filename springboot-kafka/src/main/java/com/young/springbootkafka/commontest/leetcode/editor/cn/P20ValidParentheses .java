    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public boolean isValid(String s) {
            Stack<Character> stack = new Stack<>();
            for (int i = 0; i < s.length(); i++) {
                char c = s.charAt(i);
                if (c == '(' || c == '[' || c == '{') {
                    stack.push(c);
                } else {
                    if (stack.empty()) {
                        return false;
                    }
                    char peek = stack.pop();
                    if (peek == ')' || peek == ']' || peek == '}') {
                        return false;
                    }
                    if (c == ')') {
                        if (peek != '(') {
                            return false;
                        }
                    } else if (c == ']') {
                        if (peek != '[') {
                            return false;
                        }
                    } else {
                        if (peek != '{') {
                            return false;
                        }
                    }
                }
            }
            return stack.empty();
        }
    }
