    class Solution {
        public boolean isPalindrome(int x) {
            if (x < 0) {
                return false;
            }

            return x == reverse(x);
        }

        private int reverse(int x) {
            int rev = 0;
            while (x != 0) {
                int pop = x % 10;
                if (rev > Integer.MAX_VALUE / 10
                        || (rev == Integer.MAX_VALUE / 10 && pop > 7)) {
                    return 0;
                }
                rev = rev * 10 + pop;
                x /= 10;
            }
            return rev;
        }
    }
