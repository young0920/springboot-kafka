    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int climbStairs(int n) {
            if (n == 1) {
                return 1;
            }
            if (n == 2) {
                return 2;
            }
            int res1 = 1, res2 = 2, result = 0;
            while (n - 2 > 0) {
                result = res1 + res2;
                res1 = res2;
                res2 = result;
                n--;
            }
            return result;
        }
    }
